دکمه Power سرفیس خود را فشار دهید و برای 30 ثانیه نگه دارید. در این بین ممکن است تغییراتی در نمایشگر سرفیس مشاهده کنید ، آنها را نادیده بگیرید و تا پایان 30 ثانیه دکمه Power را رها نکنید. پس از اتمام 30 ثانیه دکمه Power را رها کرده و باری دیگر کلید پاور را برای روشن شدن دستگاه فشار داده و رها کنید.
✔ اگر سرفیس شما روشن شد پس از آنکه باتری آن 40% شارژ شد ، آخرین بروزرسانی های ویندوز و سرفیس را نصب کنید.


راه حل چهارم : Two-button shutdown (فقط برای Surface Pro و SurfaceBook)

✪مهم : از این روش برای Surface RT, Surface 2 یا Surface 3 استفاده نکنید و صرفا برای مدل هایSurface Pro و Surface Book استفاده شود.

